public class SquareRoot {

        public static void main(String[] args) {
            double a = 3;
            double b = 2.5;
            double c = -0.5;

            double x1 = Math.sqrt(a);
            double x2 = Math.sqrt(b);
            double x3 = Math.sqrt(c);

            System.out.println("x1" + "=" + x1);
            System.out.println("x2" + "=" + x2);
            System.out.println("x3" + "=" + x3);
        }
}


